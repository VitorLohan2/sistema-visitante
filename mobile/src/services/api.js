const api = axios.create({
  baseURL: API_URL,
  timeout: 60000, // 60 segundos para uploads
  headers: {
    Accept: "application/json",
    "Content-Type": "application/json",
  },
});

// ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
// INTERCEPTOR DE REQUISI√á√ÉO
// Adiciona token de autentica√ß√£o automaticamente
// ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê

api.interceptors.request.use(
  async (config) => {
    try {
      const token = await AsyncStorage.getItem("@Auth:token");

      if (token) {
        config.headers.Authorization = `Bearer ${token}`;
      }

      // Para FormData, deixa o axios definir o Content-Type
      if (config.data instanceof FormData) {
        delete config.headers["Content-Type"];
      }

      // Log em desenvolvimento
      if (__DEV__) {
        console.log("üì° Requisi√ß√£o:", {
          method: config.method?.toUpperCase(),
          url: `${config.baseURL}${config.url}`,
          hasAuth: !!token,
        });
      }

      return config;
    } catch (error) {
      console.error("‚ùå Erro no interceptor de requisi√ß√£o:", error);
      return config;
    }
  },
  (error) => {
    console.error("‚ùå Erro na requisi√ß√£o:", error);
    return Promise.reject(error);
  },
);

// ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
// INTERCEPTOR DE RESPOSTA
// Trata erros e faz logout autom√°tico em 401
// ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê

api.interceptors.response.use(
  (response) => {
    if (__DEV__) {
      console.log("‚úÖ Resposta:", {
        url: response.config.url,
        status: response.status,
      });
    }
    return response;
  },
  async (error) => {
    const status = error.response?.status;
    const mensagem = error.response?.data?.error || error.message;

    if (__DEV__) {
      console.error("‚ùå Erro na resposta:", {
        url: error.config?.url,
        status,
        mensagem,
      });
    }

    // Token expirado ou inv√°lido - limpa dados de autentica√ß√£o
    if (status === 401) {
      await AsyncStorage.multiRemove(["@Auth:token", "@Auth:usuario"]);
    }

    return Promise.reject(error);
  },
);

export default api;
